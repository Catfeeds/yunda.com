	public function doSQLStr(){
		$warranty_list = CustWarranty::where('created_at','>=','1534780800000')
			->where('created_at','<','1534953600000')
			->where('warranty_status','8')
			->select('warranty_uuid','warranty_code','pro_policy_no')
			->get();
		$sql = "UPDATE cust_warranty SET warranty_status = CASE pre_policy_no ";
		foreach ($warranty_list as $value){
			$sql = $sql." WHEN "."'".$value['pro_policy_no']."'"." THEN 5 ";
		}
		$sql = $sql." END, warranty_code = CASE pre_policy_no ";
		foreach ($warranty_list as $value){
			$sql = $sql." WHEN  "."'".$value['pro_policy_no']."'"." THEN "."'".$value['warranty_code']."'";
		}
		$where = "";
		foreach ($warranty_list as $value){
			$where = $where.","."'".$value['pro_policy_no']."'";
		}
		if($where[0]==","){
			$where = ltrim($where, ",");
		}
		if($where[strlen($where)-1]==','){
			$where = rtrim($where, ",");
		}
		$sql = $sql." END WHERE pre_policy_no in (".$where.");";
		return $sql;
	}


